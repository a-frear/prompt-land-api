CREATE TABLE users (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    username TEXT NOT NULL
);

CREATE TABLE prompts (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    username TEXT NOT NULL,
    modified TIMESTAMPTZ DEFAULT now() NOT NULL,
    prompt TEXT NOT NULL
);

CREATE TABLE followers (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    userId INTEGER REFERENCES users(id) ON DELETE SET NULL,
    followingId INTEGER REFERENCES users(id) ON DELETE SET NULL
);

CREATE TABLE tags (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    tagTitle TEXT NOT NULL
);

CREATE TABLE prompt_tag (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    promptId INTEGER REFERENCES prompts(id) ON DELETE SET NULL,
    tagId INTEGER REFERENCES tags(id) ON DELETE SET NULL
);

CREATE TABLE saved_prompts (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    userId INTEGER REFERENCES users(id) ON DELETE SET NULL,
    promptId INTEGER REFERENCES prompts(id)
    ON DELETE SET NULL
);
